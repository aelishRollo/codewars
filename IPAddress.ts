export function ipsBetween(start: string, end: string): number {
    // Helper function to convert an IP address to its integer representation
    function ipToInt(ip: string): number {
      return ip.split('.').reduce((acc, octet) => acc * 256 + parseInt(octet, 10), 0);
    }
  
    const startInt = ipToInt(start);
    const endInt = ipToInt(end);
  
    return endInt - startInt;
  }
